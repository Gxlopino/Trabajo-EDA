    public void actualizarTabla() {
        model.setRowCount(0); // Limpiar tabla

        Cola<Expediente> cola = Administrador.ExpedientesPrincipal;
        Cola<Expediente> temp = new Cola<>();

        while (!cola.esVacia()) {
            Expediente e = cola.desencolar();
            Interesado i = e.getInteresado();

            Object[] fila = {
                i.getDni(),
                i.getNombre(),
                i.getTelefono(),
                i.getEmail(),
                e.getPrioridad(),
                e.getAsunto()
            };

            model.addRow(fila);
            temp.encolar(e);
        }

        while (!temp.esVacia()) {
            cola.encolar(temp.desencolar());
        }
    }

